const month = "March"

// once a key is matched and we are not using break keyword so after 
// key matched all code will executed

switch (month) {
    case "January":
        console.log("January")
        break;
    case "February":
        console.log("February")
        break;
    case "March":
        console.log("March")
        break;
    case "April":
        console.log("April")
        break;

    default:
        break;
}